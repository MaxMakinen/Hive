#include <stdlib.h>
#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*s;
	int		len;

	len = ft_strlen(s1);
	i = 0;
	s = (char *)malloc(len * sizeof (char) + 1);
	if (s == NULL)
		return (0);
	while (s1[i] != '\0')
	{
		s[i] = s1[i];
		i++;
	}
	s[i] = '\0';
	return (s);
}
