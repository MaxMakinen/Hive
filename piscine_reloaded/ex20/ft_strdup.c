#include <stdlib.h>

int	ft_strlen(char *str)
{
	int	count;

	count = 0;
	while (str[count] != '\0')
		count++;
	return (count);
}

char	*ft_strdup(char *src)
{
	int		len;
	int		count;
	char	*dest;

	count = 0;
	len = ft_strlen (src);
	dest = malloc(sizeof(dest) * len);
	if (dest == 0)
		return (0);
	while (src[count] != '\0')
	{
		dest[count] = src[count];
		count++;
	}
	dest[count] = '\0';
	return (dest);
}
